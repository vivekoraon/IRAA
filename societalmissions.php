<div class="actionplan">
	<div class="container">
		<h2 class="text-center">Action Plan</h2>
		<div class="row">
			<div class="col-md-4">
				<h6>Adoption of Villages</h6>
				<p><small>The CSM adopted five surrounding villages for development as “Model Village” as part of Unnat Bharat Abhiyan of Hon’ble Prime Minister of India.</small></p>
			</div>
			<div class="col-md-4">
				<h6>Adoption of Schools</h6>
				<p><small>The CSM proposes to adopt the basic schools in surrounding areas in phased manner to develop them as “Model Schools” so that the products of these schools can compete with their counterparts in other institutions.</small></p>
			</div>
			<div class="col-md-4 noborder">
				<h6>Cleanliness Programs in the Surrounding Areas: Under the aegis of “Swachchha Bharat Program” </h6>
				<p><small>launched by Hon’ble Prime Minister of India, CSM organizes the awareness programs at the adopted villages related to cleanliness of houses, streets and surrounding areas with the knowledge towards proper disposal of degradable and non-degradable wastes in an environmental friendly manner. For this purpose, programs of proper utilization of bio-wastes, plastic wastes and waste water are highlighted.</small></p>
			</div>
			<div class="col-md-4">
				<h6>Establishment of Educational Centers: </h6>
				<p><small>CSM integrates the educational contribution of different groups of students, faculties and staff members of IIT(ISM) Dhanbad to promote quality education and technical training to the children, of the economically poor and tribal communities in the villages under the Right to Education program of Government of India. CSM proposes to equip the nearby rural society with all the modern educational facilities through Gram Panchayat and district administration plan. The action plan may include the projection, computational and internet facilities in the identified schools and other work centers in order to connect them with the main stream. </small></p>
				
			</div>
			<div class="col-md-4">
				<h6>Sports & Physical Education Programs: </h6>
				<p><small>There are many basic schools in Dhanbad district which do not have adequate facilities for sports and physical education. Most of the students of these schools are from poor and socially backward community and they are unable to afford the cost of the education in modern schools. Thus, the children of such schools remain deprived of the basic opportunities for sports and physical education. Center of Societal Mission, IIT(ISM) Dhanbad identifies those hidden talents and promote them. </small></p>
			</div>
			<div class="col-md-4 noborder">
				<h6>Health Care Services to the Community:  </h6>
				<p><small>CSM organizes Health Care Programs (Free Medical Camps) for school children, as well as the villagers. CSM also proposes to provide emergency medical help like “Ambulance Services”, “Blood Donation”, etc. to the people of surrounding localities. </small></p>
			</div>
			<div class="col-md-4 noborderbottom">
				<h6>Fulfilling the needs of the poor people:  </h6>
				<p><small>CSM proposes to organize programs to fulfill the basic needs of socially down-trodden people by distributing clothes, medicines, bed-sheets, blankets, etc. The center also provides the service of testing the quality of drinking water in order to avoid the waterborne diseases. </small></p>
			</div>
			<div class="col-md-4 noborderbottom">
				<h6>Organizing Seminars and Workshops:  </h6>
				<p><small>CSM will focus on conducting workshops in regular manner to increase awareness on “Save Fuel”, “Save Electricity”, “Use of Petroleum Products”, “Use of Solar Energy”, “Firefighting”, “Reduce Pollution”, “No tobacco”, “No Plastics”, “Protection from HIV”, “Reduce, Reuse and Recycle Wastes” “Ethical Values”, etc.</small></p>
			</div>
			<div class="col-md-4 noborder noborderbottom">
				<h6>Providing Expert Services: </h6>
				<p><small>Hon’ble Prime Minister of India has expressed about the need for extending an expert service to the farmers in order to increase the productivity of crops, vegetables and grains. He has categorically mentioned towards the need of “Soil Testing Laboratory” to provide the expert advice to the farmers in order to increase the land productivity. CSM proposes to extend the services of soil testing and water testing facilities for the farmers residing in nearby vicinity. An awareness program regarding dis-advantage of continuous uses of chemical fertilizers is proposed be organized for the villagers. </small></p>
			</div>
